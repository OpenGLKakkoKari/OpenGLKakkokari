#include "DirectXTexture.h"


using DirectX::Texture;


/*  実装
******************************************************************************/

/******************************************************************************
@brief  コンストラクタ
******************************************************************************/

DirectX::Texture::Texture() : pTex(NULL)
{
}

/******************************************************************************
@brief  デストラクタ
******************************************************************************/

DirectX::Texture::~Texture()
{
    if (pTex != NULL)
    {
        pTex->Release();
        pTex = NULL;
    }
}

/******************************************************************************
@brief  テクスチャの開放
******************************************************************************/

void DirectX::Texture::Release()
{
    if (IsReleased())
    {
        if (pTex != NULL)
        {
            pTex->Release();
            pTex = NULL;
        }
    }
    Framework::Texture::Release();
}

