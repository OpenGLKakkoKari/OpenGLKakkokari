using namespace Framework;


/*  実装
******************************************************************************/

/******************************************************************************
@brief  コンストラクタ
******************************************************************************/

SceneManager::SceneManager() : nowScene(NULL)
{
}

/******************************************************************************
@brief  デストラクタ
******************************************************************************/

SceneManager::~SceneManager()
{
    if (nowScene)
    {
        nowScene->Uninit();
        delete nowScene;
        nowScene = NULL;
    }
}

/******************************************************************************
@brief  更新処理
******************************************************************************/

void SceneManager::Update()
{
    nowScene->Update();
}

/******************************************************************************
@brief  ステート変更処理
******************************************************************************/

void SceneManager::SetScene(Scene* newScene)
{
    if (!newScene)
        return;
    if (nowScene)
    {
        nowScene->Uninit();
        delete nowScene;
    }
    newScene->Init();
    nowScene = newScene;
}

