#include "DebugTimer.h"

using Framework::DebugTimer;


/*  実装
******************************************************************************/

/******************************************************************************
@brief  コンストラクタ
******************************************************************************/

DebugTimer::DebugTimer()
{

}

/******************************************************************************
@brief  デストラクタ
******************************************************************************/

DebugTimer::~DebugTimer()
{
}

/******************************************************************************
@brief  計測開始
******************************************************************************/

void DebugTimer::Start()
{
    if (!QueryPerformanceFrequency(&freq))      // 単位習得
        return;
    QueryPerformanceCounter(&start);
}

/******************************************************************************
@brief  計測終了
******************************************************************************/

void DebugTimer::End()
{
    QueryPerformanceCounter(&end);
}

/******************************************************************************
@brief  秒取得
@return float       秒
******************************************************************************/

float DebugTimer::GetSecond()
{
    return (float)(end.QuadPart - start.QuadPart) / freq.QuadPart;
}

