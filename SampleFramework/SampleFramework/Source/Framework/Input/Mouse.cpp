/******************************************************************************
@brief  コンストラクタ
******************************************************************************/

Mouse::Mouse(HWND hWnd) : hWnd_(hWnd)
{
}

/******************************************************************************
@brief  デストラクタ
******************************************************************************/

Mouse::~Mouse()
{
}

/******************************************************************************
@brief  更新処理
******************************************************************************/

void Mouse::Update()
{
    POINT pos;
    GetCursorPos(&pos);
    ScreenToClient(hWnd_, &pos);

    cursorPos_ = Vector2(pos.x, pos.y);
}

